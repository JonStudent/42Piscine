#include <unistd.h>

void	ft_print_comb2(void)
{
	int		n1;
	int		n2;
	char	comb[5];

	n1 = -1;
	while (n1++ < 98)
	{
		n2 = n1;
		while (n2++ < 99)
		{
			comb[0] = n1 / 10 + '0';
			comb[1] = n1 % 10 + '0';
			comb[2] = ' ';
			comb[3] = n2 / 10 + '0';
			comb[4] = n2 % 10 + '0';
			write (1, &comb, 5);
			if (n1 != 98)
			{
				write(1, ", ", 2);
			}
		}
	}
}
